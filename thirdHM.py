# Ввод пользователем верхнего предела
n = int(input("Введите натуральное число (не более 100): "))

# Проверка на корректность введенного числа
if 1 <= n <= 100:
    # Вычисление суммы кубов
    total_sum = sum(i ** 3 for i in range(1, n + 1))
    
    # Вывод результата
    print(f"Сумма кубов натуральных чисел от 1 до {n} равна: {total_sum}")
else:
    print("Ошибка: Число должно быть от 1 до 100.")


