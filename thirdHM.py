# Функция для вывода таблицы умножения
def print_multiplication_table():
    # Заголовок таблицы
    print("Таблица умножения")
    print("-------------------")
    
    # Вывод верхнего ряда заголовков
    header = "   |"
    for i in range(1, 10):
        header += f"{i:3} "
    print(header)
    
    print("-------------------")
    
    # Вывод строк таблицы
    for i in range(1, 10):
        row = f"{i:2} |"
        for j in range(1, 10):
            row += f"{i * j:3} "
        print(row)

# Вызов функции
print_multiplication_table()




